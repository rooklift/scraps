def n_to_sgf(n):
    x = n % 19
    y = n // 19
    return chr(x + 97) + chr(y + 97)

tokens = [c for c in diagram if c in [".", "O", "X", ","]]

ab = []
aw = []

for n, c in enumerate(tokens):

	if c == "X":
		ab.append(n_to_sgf(n))

	if c == "O":
		aw.append(n_to_sgf(n))

print("(;AB[{}]AW[{}])".format("][".join(ab), "][".join(aw)))

